function setup() {  
      
    createCanvas(400, 300);  
    iframeBackgroundColor('#0F1121');
    iframeBoxShadow('inset 15px 15px 15px rgba(0,0,0,.6)');
    iframeBorder('2px solid #22244B');
    iframeBorderRadius('10px');
    iframePadding('10px');
}  
  
function draw() {  
      
    strokeWeight(20);   
    stroke('green');   
    point(50, 50);     
    point(50, 250);     
    point(350, 50);     
    point(350, 250);     
    point(200, 150); 
    
} 

